/**
 * ===========================================================================
 * 策略模式（Strategy Pattern）— C语言实现
 * ===========================================================================
 *
 * 【模式名称】策略模式（Strategy Pattern）
 * 【类型】行为型模式（Behavioral Pattern）
 * 【解决什么问题】
 *   当一个系统中有多种算法/行为，且需要在运行时动态切换时，
 *   如果用 if-else 或 switch-case 来选择实现，会导致：
 *   - 代码臃肿（每加一个策略要改原类）
 *   - 违反开闭原则（对扩展开放，对修改封闭）
 *   - 难以单独测试每种策略
 *
 * 【策略模式的核心思想】
 *   把"会变化的行为"封装成独立的策略对象，
 *   上下文（Context）持有策略接口，运行时可以切换不同策略，
 *   而上下文本身保持不变。
 *
 * 【嵌入式/PCIe 实战场景】
 *   - DMA 传输完成后的回调策略：中断通知 / 填充FIFO / 记录日志
 *   - LED 亮度映射策略：线性 / 指数（人眼感知） / S曲线
 *   - PCIe 设备访问策略：MMIO / 配置空间 / BAR映射
 *   - 固件升级策略：双Bank / 单Bank回退 / OTA差分
 *
 * 【对应《Head First 设计模式》第一章】
 * ===========================================================================
 */

#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <stdbool.h>
#include <math.h>

/*====================================================================
 * 示例1：排序策略 — 最贴近实战的例子
 *
 * 场景：固件里需要排序功能，最初只需要升序，后面需要降序、
 *       按绝对值排序等多种策略。用策略模式避免重复写排序逻辑。
 *
 * 【策略模式的嵌入式映射】
 *   CompareFunc        → 策略接口（类似 PCIe 事务层的事务类型）
 *   sort_generic()     → 上下文（类似 DMA 控制器，不关心具体比较逻辑）
 *   compare_int_asc()  → 具体策略A（升序，类似 PCIe Gen1 x1）
 *   compare_int_desc() → 具体策略B（降序，类似 PCIe Gen2 x1）
 *   compare_int_abs()  → 具体策略C（绝对值排序，类似 Gen3 x1）
 *====================================================================*/

// -----------------------------------------------------------------------------
// 策略接口：比较函数指针
// -----------------------------------------------------------------------------
// 【为什么用函数指针？】
//   C语言没有虚函数表，我们用函数指针模拟"接口"。
//   把"算法"当成数据一样传递——运行时决定调用哪个函数。
//   对应《Head First》里的 FlyBehavior 接口。
//
// 【参数含义】
//   a, b : 待比较的两个元素的指针（void* 实现泛型）
// 【返回值】
//   > 0 : a > b
//   = 0 : a == b
//   < 0 : a < b
// 【调用时机】
//   sort_generic() 内部每次需要比较两个元素时调用
// -----------------------------------------------------------------------------
typedef int (*CompareFunc)(const void *a, const void *b);

// -----------------------------------------------------------------------------
// 上下文：泛型排序
// -----------------------------------------------------------------------------
// 【参数】
//   arr       : 待排序数组的首地址（void* 实现任意类型支持）
//   n         : 数组元素个数
//   elem_size : 每个元素的大小（字节），用于 memcpy 交换
//   cmp       : 比较策略函数指针（运行时注入）
// 【返回值】无（原地排序）
// 【调用时机】固件初始化阶段、测试用例、数据处理模块
//
// 【实现细节：为什么用 char* 而不用 void* 算术？】
//   C语言不允许 void* 做指针算术，所以强转成 char*（1字节步长）
//   base + i * elem_size 等价于 &arr[i]，但泛型
//   交换用 memcpy 而非直接赋值，支持大结构体（如 PCIe 描述符）
// -----------------------------------------------------------------------------
void sort_generic(void *arr, size_t n, size_t elem_size, CompareFunc cmp) {
    char *base = (char*)arr;
    for (size_t i = 0; i < n; i++) {
        for (size_t j = i + 1; j < n; j++) {
            // cmp() 返回值 > 0 表示 a > b，需要交换
            if (cmp(base + i * elem_size, base + j * elem_size) > 0) {
                // VLA（变长数组）临时缓冲区，大小在运行时确定
                // 等价于 C++ 的 swap，但支持任意大小的结构体
                char tmp[elem_size];
                memcpy(tmp, base + i * elem_size, elem_size);
                memcpy(base + i * elem_size, base + j * elem_size, elem_size);
                memcpy(base + j * elem_size, tmp, elem_size);
            }
        }
    }
}

// -----------------------------------------------------------------------------
// 具体策略1：整数升序
// 【嵌入式映射】固件日志等级过滤（升序 = 数字小优先）
// -----------------------------------------------------------------------------
int compare_int_asc(const void *a, const void *b) {
    int ia = *(const int*)a;
    int ib = *(const int*)b;
    return ia - ib;  // 溢出风险：INT_MIN - INT_MAX 会在某些平台出问题
                     // 嵌入式安全版：(ia < ib) ? -1 : (ia > ib)
}

// -----------------------------------------------------------------------------
// 具体策略2：整数降序
// 【嵌入式映射】中断优先级排序（高优先级数值大，排在前面）
// -----------------------------------------------------------------------------
int compare_int_desc(const void *a, const void *b) {
    int ia = *(const int*)a;
    int ib = *(const int*)b;
    return ib - ia;  // 逆序：b - a
}

// -----------------------------------------------------------------------------
// 具体策略3：按绝对值排序
// 【嵌入式映射】信号强度排序、误差值排序（只看幅度，不看符号）
// -----------------------------------------------------------------------------
int compare_int_abs(const void *a, const void *b) {
    int ia = abs(*(const int*)a);
    int ib = abs(*(const int*)b);
    return ia - ib;
}

// -----------------------------------------------------------------------------
// 具体策略4：按字符串长度排序
// 【嵌入式映射】固件配置项按关键字长度匹配（短的先匹配）
// -----------------------------------------------------------------------------
int compare_str_len(const void *a, const void *b) {
    const char *sa = *(const char**)a;
    const char *sb = *(const char**)b;
    return (int)strlen(sa) - (int)strlen(sb);
}

// -----------------------------------------------------------------------------
// 辅助函数：打印整数数组
// -----------------------------------------------------------------------------
void print_int_array(const int *arr, size_t n, const char *label) {
    printf("  %s: ", label);
    for (size_t i = 0; i < n; i++) printf("%d ", arr[i]);
    printf("\n");
}

// -----------------------------------------------------------------------------
// 演示函数：排序策略
// -----------------------------------------------------------------------------
void demo_sort_strategy(void) {
    printf("=== 示例1：排序策略 ===\n");

    int arr[] = {3, 1, 4, 1, 5, 9, 2, 6};
    size_t n = sizeof(arr) / sizeof(arr[0]);

    sort_generic(arr, n, sizeof(int), compare_int_asc);
    print_int_array(arr, n, "升序");

    sort_generic(arr, n, sizeof(int), compare_int_desc);
    print_int_array(arr, n, "降序");

    int arr2[] = {-3, 1, -4, 1, -5, 2};
    sort_generic(arr2, 6, sizeof(int), compare_int_abs);
    print_int_array(arr2, 6, "绝对值排序");

    const char *strs[] = {"hello", "a", "world!", "hi"};
    sort_generic(strs, 4, sizeof(char*), compare_str_len);
    printf("  字符串长度排序: ");
    for (int i = 0; i < 4; i++) printf("\"%s\" ", strs[i]);
    printf("\n\n");
}


/*====================================================================
 * 示例2：出行策略 — 模拟《Head First》鸭子的例子
 *
 * 【嵌入式映射】
 *   TravelFn           → 固件动作策略函数指针
 *   travel_by_plane()  → PCIe Gen4 x16 传输（快速）
 *   travel_by_train()  → PCIe Gen1 x1 传输（稳定/兼容）
 *   travel_by_bike()   → Polling 轮询方式（低功耗）
 *====================================================================*/

// -----------------------------------------------------------------------------
// 抽象策略接口：出行方式
// -----------------------------------------------------------------------------
// 【为什么返回 void？】
//   策略函数负责打印输出，上下文不需要知道具体策略的实现细节
//   固件场景下，策略函数可能操作硬件寄存器（GPIO/Timer等）
// -----------------------------------------------------------------------------
typedef void (*TravelFn)(const char *dest);

// -----------------------------------------------------------------------------
// 策略实现：飞机
// 【嵌入式映射】高速传输路径，如 DMA 零拷贝模式
// -----------------------------------------------------------------------------
static void travel_by_plane(const char *dest) {
    printf("✈️  乘坐飞机前往 %s，3小时后到达\n", dest);
}

// -----------------------------------------------------------------------------
// 策略实现：火车
// 【嵌入式映射】稳定传输路径，如可靠的多包确认机制
// -----------------------------------------------------------------------------
static void travel_by_train(const char *dest) {
    printf("🚂  乘坐火车前往 %s，8小时后到达\n", dest);
}

// -----------------------------------------------------------------------------
// 策略实现：骑车
// 【嵌入式映射】低功耗轮询模式，如中断驱动的低频采样
// -----------------------------------------------------------------------------
static void travel_by_bike(const char *dest) {
    printf("🚲  骑自行车前往 %s，2小时后到达\n", dest);
}

// -----------------------------------------------------------------------------
// 上下文：旅行规划器
// -----------------------------------------------------------------------------
// 【设计意图】
//   规划器本身不决定出行方式，只持有策略指针
//   策略可以在运行时切换，上下文代码不需要改变
//   这就是"把变化的部分封装起来"的原则
//
// 【嵌入式类比】
//   TravelPlanner 类似固件的"任务调度器"，
//   它本身不执行具体操作，只调用注册的回调函数
// -----------------------------------------------------------------------------
typedef struct {
    TravelFn go;              // 策略指针，可运行时替换
    const char *planner_name; // 规划器名称（上下文状态）
} TravelPlanner;

// -----------------------------------------------------------------------------
// 运行时切换策略
// 【参数】p: 规划器指针，fn: 新策略函数
// 【调用时机】系统模式切换时（如低功耗模式切换、高性能模式切换）
// -----------------------------------------------------------------------------
void planner_set_strategy(TravelPlanner *p, TravelFn fn) {
    p->go = fn;
}

// -----------------------------------------------------------------------------
// 执行出行
// 【参数】p: 规划器指针，dest: 目的地
// 【调用时机】用户/系统触发出行请求时
// -----------------------------------------------------------------------------
void planner_travel(TravelPlanner *p, const char *dest) {
    p->go(dest);
}

// -----------------------------------------------------------------------------
// 演示函数：出行策略
// -----------------------------------------------------------------------------
void demo_travel_strategy(void) {
    printf("=== 示例2：出行策略 ===\n");

    TravelPlanner planner = {
        .go = travel_by_train,   // 默认策略：火车
        .planner_name = "我的旅行规划"
    };

    printf("使用%s出行：\n", planner.planner_name);
    printf("  去北京：");
    planner.go("北京");

    planner.go = travel_by_plane;   // 运行时切换策略：飞机
    printf("  去上海：");
    planner.go("上海");

    planner.go = travel_by_bike;    // 运行时切换策略：骑车
    printf("  去郊区：");
    planner.go("郊区");
    printf("\n");
}


/*====================================================================
 * 示例3：LED 亮度控制策略 — 嵌入式实战
 *
 * 【问题背景】
 *   LED 的亮度感知是人眼非线性的：
 *   - 线性亮度：PWM 50% 时人眼感知不到 50% 亮度，实际只有 ~25%
 *   - 指数亮度：PWM 50% 时人眼感知约 50%
 *   不同场景需要不同映射曲线，直接改策略函数即可
 *
 * 【PCIe 类比】
 *   PCIe LTSSM 的电气空闲退出时间，不同 Gen 速率有不同公式
 *   可以用策略模式封装不同的退出时间计算公式
 *====================================================================*/

typedef int BrightnessLevel;  // 亮度级别：0-1000，对应 PWM 占空比

// -----------------------------------------------------------------------------
// 亮度映射策略接口
// 【参数】target: 目标亮度（0-1000）
// 【返回值】实际 PWM 占空比（经过映射曲线）
// 【调用时机】led_set_target() 调用时
// -----------------------------------------------------------------------------
typedef BrightnessLevel (*BrightnessMapFn)(BrightnessLevel target);

// -----------------------------------------------------------------------------
// 策略A：线性映射（直线）
// 【应用场景】教学演示、简单指示灯
// -----------------------------------------------------------------------------
static BrightnessLevel brightness_linear(BrightnessLevel target) {
    return target;  // 1:1 映射，无补偿
}

// -----------------------------------------------------------------------------
// 策略B：指数映射（平方）
// 【应用场景】需要人眼感知线性的照明场景
// 【原理】人眼对亮度的感知是对数级的，物理线性 PWM 显得前面亮后面暗
//         用 target^2 补偿：50% PWM 时物理功率 = 25%，人眼感知 ≈ 50%
// -----------------------------------------------------------------------------
static BrightnessLevel brightness_exp(BrightnessLevel target) {
    float t = (float)target / 1000.0f;
    float result = t * t;  // 平方补偿人眼感知非线性
    return (BrightnessLevel)(result * 1000.0f);
}

// -----------------------------------------------------------------------------
// 策略C：S曲线映射（Logistic）
// 【应用场景】氛围灯、渐变呼吸灯
// 【原理】Logistic 曲线在中间变化缓慢，两端变化快
//         适合模拟"柔和过渡"的视觉感受
// -----------------------------------------------------------------------------
static BrightnessLevel brightness_s_curve(BrightnessLevel target) {
    float t = (float)target / 1000.0f;
    // exp(-10*(t-0.5)) 在 t=0.5 时 = 1，S = 0.5
    // t<0.5 时 S<0.5（暗），t>0.5 时 S>0.5（亮）
    float s = 1.0f / (1.0f + exp(-10.0f * (t - 0.5f)));
    return (BrightnessLevel)(s * 1000.0f);
}

// -----------------------------------------------------------------------------
// LED 控制器（上下文）
// 【字段】
//   map_brightness : 亮度映射策略指针（可运行时切换）
//   current        : 当前实际亮度
//   led_name       : LED 名称（用于调试输出）
// -----------------------------------------------------------------------------
typedef struct {
    BrightnessMapFn map_brightness;  // 策略指针，NULL = 禁用映射
    BrightnessLevel current;        // 当前实际亮度
    const char *led_name;           // 设备名称
} LEDController;

// -----------------------------------------------------------------------------
// 设置 LED 目标亮度
// 【参数】
//   led   : LED 控制器指针
//   target: 目标亮度（0-1000）
// 【调用时机】固件接收亮度设置命令时
// -----------------------------------------------------------------------------
void led_set_target(LEDController *led, BrightnessLevel target) {
    if (led->map_brightness) {
        // 有映射策略：经过策略函数转换
        led->current = led->map_brightness(target);
    } else {
        // 无策略：直辖市
        led->current = target;
    }
    printf("  %s: 目标=%d -> 实际=%d\n",
           led->led_name, target, led->current);
}

// -----------------------------------------------------------------------------
// 演示函数：LED 亮度策略
// -----------------------------------------------------------------------------
void demo_led_brightness(void) {
    printf("=== 示例3：LED亮度控制策略（嵌入式实战） ===\n");

    LEDController led = {
        .map_brightness = brightness_linear,
        .current = 0,
        .led_name = "LED1"
    };

    printf("线性亮度策略：\n");
    led_set_target(&led, 500);  // 目标 50%，实际 50%（线性）

    printf("\n指数亮度策略（人眼感知）：\n");
    led.map_brightness = brightness_exp;  // 运行时切换策略
    led_set_target(&led, 500);  // 目标 50%，实际 ~25%（指数补偿）

    printf("\nS曲线亮度策略（氛围灯）：\n");
    led.map_brightness = brightness_s_curve;  // 运行时切换策略
    led_set_target(&led, 500);  // 目标 50%，实际 ≈50%（S曲线特性）
    printf("\n");
}


/*====================================================================
 * 示例4：PCIe DMA 回调策略 — Linux内核实战
 *
 * 【问题背景】
 *   DMA 传输完成后，CPU 需要知道传输结果。
 *   不同业务场景对 DMA 完成的处理方式不同：
 *   - 网络驱动：DMA 完成后发网络中断通知协议栈
 *   - 存储驱动：DMA 完成后填充数据到 Ring Buffer
 *   - 日志驱动：DMA 完成后简单记录状态
 *
 *   用 if-else 会导致驱动代码膨胀，
 *   用策略模式把"完成后的动作"封装成可插拔的回调策略
 *
 * 【Linux 内核真实场景】
 *   kernel/dma/dmaengine.h 中的 dma_async_tx_callback
 *   drivers/pci/pcie/aer.c 中的错误恢复回调
 *====================================================================*/

// -----------------------------------------------------------------------------
// DMA 传输状态枚举
// 【嵌入式说明】
//   DMA_STATUS_PENDING : 传输已提交，等待硬件完成
//   DMA_STATUS_COMPLETE : 传输成功完成
//   DMA_STATUS_ERROR    : 传输失败（总线错误、超时、CRC错误等）
// -----------------------------------------------------------------------------
typedef enum {
    DMA_STATUS_PENDING,
    DMA_STATUS_COMPLETE,
    DMA_STATUS_ERROR
} DmaStatus;

// -----------------------------------------------------------------------------
// 模拟PCIe设备结构体
// 【嵌入式映射】
//   name     → 设备的 BDF 地址（Bus:Device.Function）
//   irq_wake → 是否支持唤醒式中断
// -----------------------------------------------------------------------------
typedef struct {
    const char *name;
    int irq_wake;
} PciDev;

// -----------------------------------------------------------------------------
// 模拟 DMA Ring Buffer 的 FIFO 头
// 【嵌入式映射】
//   head → 下一条可写位置（生产者指针）
//   tail → 下一条可读位置（消费者指针）
//   典型用在 PCIe 的描述符环（Descriptor Ring）
// -----------------------------------------------------------------------------
typedef struct {
    int head;
    int tail;
} Fifo;

// -----------------------------------------------------------------------------
// DMA 传输请求结构体（上下文 + 请求信息）
// 【字段解析】
//   src/dst       : 源/目的物理地址（PCIe 总线地址）
//   len           : 传输字节数
//   status        : 当前传输状态
//   callback      : 完成回调策略函数指针（核心！）
//   callback_param: 回调参数（通常传设备结构体指针）
// -----------------------------------------------------------------------------
typedef struct {
    void *src;
    void *dst;
    size_t len;
    DmaStatus status;
    void (*callback)(void *param, DmaStatus status);  // 回调策略指针
    void *callback_param;                              // 回调参数
} DmaRequest;

// -----------------------------------------------------------------------------
// 提交 DMA 传输请求
// 【参数】req: DMA 请求指针
// 【调用时机】CPU 发起 DMA 传输前
// 【行为】设置状态为 PENDING，实际硬件传输由 DMA 控制器异步完成
// -----------------------------------------------------------------------------
void dma_submit(DmaRequest *req) {
    req->status = DMA_STATUS_PENDING;
    printf("  [DMA] 提交传输: %zu bytes\n", req->len);
}

// -----------------------------------------------------------------------------
// DMA 传输完成处理
// 【参数】
//   req   : DMA 请求指针
//   status: 传输结果状态
// 【调用时机】DMA 中断服务程序（ISR）被触发时
// 【行为】
//   1. 更新请求状态
//   2. 调用注册的业务回调策略
// -----------------------------------------------------------------------------
void dma_complete(DmaRequest *req, DmaStatus status) {
    req->status = status;
    if (req->callback) {
        // 【关键】回调策略在运行时通过函数指针注入
        // 同一个 DMA 控制器可以服务不同业务，互不干扰
        req->callback(req->callback_param, status);
    }
}

// -----------------------------------------------------------------------------
// 策略A：发中断通知
// 【应用场景】网络驱动：DMA 完成后触发 MSI 中断通知协议栈处理数据
// 【实现要点】
//   - (void)status 是消除"未使用参数"警告的惯用法
//   - 实际驱动中会调用 netif_rx() 或类似接口
// -----------------------------------------------------------------------------
static void dma_callback_interrupt(void *param, DmaStatus status) {
    (void)status;
    PciDev *dev = (PciDev *)param;
    printf("  [策略A] DMA完成，发中断给%s\n", dev->name);
}

// -----------------------------------------------------------------------------
// 策略B：填充 FIFO
// 【应用场景】存储驱动：DMA 完成后将数据推入 Ring Buffer 等待上层处理
// 【实现要点】
//   param 传的是 Fifo 结构体指针（也可以传 Ring Buffer 指针）
// -----------------------------------------------------------------------------
static void dma_callback_fifo(void *param, DmaStatus status) {
    (void)param; (void)status;
    printf("  [策略B] DMA完成，数据填充FIFO\n");
}

// -----------------------------------------------------------------------------
// 策略C：记录日志
// 【应用场景】调试/诊断：仅记录 DMA 完成状态，不做业务处理
// -----------------------------------------------------------------------------
static void dma_callback_log(void *param, DmaStatus status) {
    (void)param;
    printf("  [策略C] DMA状态: %s\n",
           status == DMA_STATUS_COMPLETE ? "成功" : "失败");
}

// -----------------------------------------------------------------------------
// 演示函数：PCIe DMA 回调策略
// -----------------------------------------------------------------------------
void demo_pcie_dma(void) {
    printf("=== 示例4：PCIe DMA回调策略（Linux内核实战） ===\n");

    PciDev dev = {.name = "0000:01:00.0", .irq_wake = 0};
    Fifo fifo = {.head = 0, .tail = 0};
    (void)fifo;  // fifo 在策略B中使用，此处避免未使用警告

    DmaRequest req = {
        .src = (void*)0x100000,   // 物理地址：PCIe 总线地址空间
        .dst = (void*)0x200000,
        .len = 4096,              // 4KB：一个 PCIe MRd/MWr 的典型传输大小
    };

    // 策略A：发中断（模拟网络驱动）
    req.callback = dma_callback_interrupt;
    req.callback_param = &dev;
    dma_submit(&req);
    dma_complete(&req, DMA_STATUS_COMPLETE);

    printf("\n");
    // 策略B：填充 FIFO（模拟存储驱动）
    req.callback = dma_callback_fifo;
    req.callback_param = &fifo;
    dma_submit(&req);
    dma_complete(&req, DMA_STATUS_COMPLETE);

    printf("\n");
    // 策略C：记录日志（模拟调试模式）
    req.callback = dma_callback_log;
    req.callback_param = NULL;
    dma_submit(&req);
    dma_complete(&req, DMA_STATUS_ERROR);
    printf("\n");
}


